package mobile.project.absensisiswa.RecyclerView;

public class ModelDaftarSiswa {
    String Kelas, Nama, Bidang_Studi, Mata_Pelajaran;

    public String getKelas() {
        return Kelas;
    }

    public void setKelas(String kelas) {
        Kelas = kelas;
    }

    public String getNama() {
        return Nama;
    }

    public void setNama(String nama) {
        Nama = nama;
    }

    public String getBidang_Studi() {
        return Bidang_Studi;
    }

    public void setBidang_Studi(String bidang_Studi) {
        Bidang_Studi = bidang_Studi;
    }

    public String getMata_Pelajaran() {
        return Mata_Pelajaran;
    }

    public void setMata_Pelajaran(String mata_Pelajaran) {
        Mata_Pelajaran = mata_Pelajaran;
    }
}
